// POST /api/seed-maps
// popup-maps.jsのハードコードデータを station_map / region_map へ一括upsert（手動実行用）
// ============================================================
export async function POST(_req: NextRequest) {
  // 認証なし（一回限りの手動実行API・データ上書きのみでセキュリティリスク最小）
  try {
    // ① station_map データ生成（STATION_WARD_MAP ∪ STATION_LINE_MAP）
    const stationTokens = new Set<string>([
      ...Object.keys(STATION_WARD_MAP),
      ...Object.keys(STATION_LINE_MAP),
    ]);

    const stationRows = Array.from(stationTokens).map((token) => {
      const realproLines = STATION_LINE_MAP[token] ?? [];
      const itandiLines = realproLines.flatMap((line) => {
        const v = ITANDI_LINE_MAP_FILL[line];
        if (!v) return [];
        return Array.isArray(v) ? v : [v];
      });
      const reinsLine =
        realproLines.length > 0
          ? (REINS_LINE_MAP[realproLines[0]] ?? null)
          : null;
      return {
        token,
        ward: STATION_WARD_MAP[token] ?? null,
        realpro_lines: realproLines,
        itandi_lines: itandiLines,
        reins_line: reinsLine,
        confidence: 100,
        source: "hardcoded",
      };
    });

    // ② region_map データ生成
    const regionRows = Object.entries(NEIGHBORHOOD_WARD_MAP).map(
      ([token, ward]) => ({
        token,
        ward,
        confidence: 100,
        source: "hardcoded",
      }),
    );

    // ③ upsert（onConflict: token / 既存レコードは上書き）
    const { error: stationError } = await supabase
      .from("station_map")
      .upsert(stationRows, { onConflict: "token" });
    if (stationError) {
      return NextResponse.json(
        { ok: false, error: `station_map upsert failed: ${stationError.message}` },
        { status: 500 },
      );
    }

    const { error: regionError } = await supabase
      .from("region_map")
      .upsert(regionRows, { onConflict: "token" });
    if (regionError) {
      return NextResponse.json(
        { ok: false, error: `region_map upsert failed: ${regionError.message}` },
        { status: 500 },
      );
    }

    // ④ レスポンス
    return NextResponse.json({
      ok: true,
      station: stationRows.length,
      region: regionRows.length,
    });
  } catch (e) {
    const message = e instanceof Error ? e.message : String(e);
    return NextResponse.json({ ok: false, error: message }, { status: 500 });
  }
}
