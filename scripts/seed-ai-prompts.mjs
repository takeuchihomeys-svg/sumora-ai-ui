for (const row of rows) {
  const { error } = await supabase
    .from("ai_prompts")
    .upsert({ key: row.key, content: row.content, updated_at: new Date().toISOString() }, { onConflict: "key" });

  if (error) {
    console.error(`❌ ${row.key}:`, error.message);
  } else {
    console.log(`✅ ${row.key} (${row.content.length}文字) 保存完了`);
  }
}
